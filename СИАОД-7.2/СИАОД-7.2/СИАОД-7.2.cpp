#include <iostream>

using namespace std;

int main()
{
    int MATRIX[8][8] = { {-1, 23, 12,  0,  0,  0,  0,  0},
                         {23, -1, 25,  0, 22,  0,  0, 35},
                         {12, 25, -1, 18,  0,  0,  0,  0},
                         { 0,  0, 18, -1,  0, 20,  0,  0},
                         { 0, 22,  0,  0, -1, 23, 14,  0},
                         { 0,  0,  0, 20, 23, -1, 24,  0},
                         { 0,  0,  0,  0, 14, 24, -1, 16},
                         { 0, 35,  0,  0,  0,  0, 16, -1} };

    for (int i = 0; i < 8; i++) {
        for (int j = 0; j < 8; j++) {
            if (MATRIX[i][j] != 0 && i!=j) {
                cout << i+1 << "-" << j+1 << "    " << MATRIX[i][j] << endl;
            }
        }
    }
}